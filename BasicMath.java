public class BasicMath 
{
	public static int add(int a, int b)
	{
		int add = a+b;
		return add;
	}
	public static int subtract(int c, int d)
	{
		int subtract = c-d;
		return subtract;
	}
	public static int multiply(int e, int f)
	{
		int multiply = e*f;
		return multiply;
	}
	public static double divide(int g, int h)
	{
		double divide = ((g*10.0)/h)/10.0;
		return divide;
	}
}